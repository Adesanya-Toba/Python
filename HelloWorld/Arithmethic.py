print(10 / 3)  # returns floating point number
print(10 // 3)  # returns int
print(10 % 3)  # returns remainder

print(10 ** 3)  # performs the exponent (raises to power 3)

# Operator precedence
# parenthesis () -> exponent ** -> multiplication * or division / // ->
# addition + or subtraction -
a = 10
a = a + 3
a += 3  # Augmented assignment
print(a)
print()

# Math functions
import math

x = 2.9
print(round(x))  # round: rounds up
print(abs(-2.9))  # abs: absolute value

print(math.ceil(x))  # ceil: prints the upper round value
print(math.floor(x))  # floor: prints the lower round value

